<script>

    function save_property_by_click(id) {

        var element = document.getElementById('element' + id);



        var save = '<i class="fa fa-heart" aria-hidden="true"></i> Save';

        var saved = '<i class="fa fa-check" aria-hidden="true"></i> Saved';



        var save_status = "";



        //console.log(save + ' = ' + element.innerHTML);



        if (element.innerHTML === save) {

            element.innerHTML = saved;

            save_status = 1;

        } else if (element.innerHTML === saved) {

            element.innerHTML = save;

            save_status = 0;

        }



        $.ajax({

            type: "POST",

            url: "<?php echo base_url(); ?>" + "property/ajax_saved_properties",

            data: {id: id, status: save_status},

            dataType: "text",

            cache: false,

            success:

                function (data) {

                    $("#saved_pro_hide").hide();

                    $("#saved_pro").html(data);

                }

        });

    }

</script>

